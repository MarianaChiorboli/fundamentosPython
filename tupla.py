filmesTuple = ( "Branca de Neve", "Cinderela", "Frozen",
              "Bela Adormecida", "Interstellar")

# 1 - Buscar os dois primeiros itens da tupla
print(filmesTuple[:2])

# 2 - buscar o último item da tupla
print(filmesTuple[-1])

# 3 - Buscar filme até determinada posição 
print(filmesTuple[:3])

# 4 - Buscar filmes de uma posição em diante 
